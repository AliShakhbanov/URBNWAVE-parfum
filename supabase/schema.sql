create extension if not exists pgcrypto;

create table if not exists public.products (
  id bigint generated by default as identity primary key,
  name text not null,
  category text not null,
  price integer not null,
  image text not null,
  description text not null,
  volume integer not null,
  family text not null check (family in ('цитрус', 'древесный', 'восточный', 'цветочный')),
  is_new boolean default false,
  is_hit boolean default false,
  sale_percent integer,
  popularity integer default 0,
  release_year integer default extract(year from now()),
  longevity text default 'средний' check (longevity in ('легкий', 'средний', 'стойкий')),
  inspired_by text,
  notes jsonb default '{"top":[],"heart":[],"base":[]}'::jsonb,
  scenarios text[] default '{}',
  gallery text[] default '{}',
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

create table if not exists public.orders (
  id uuid primary key default gen_random_uuid(),
  status text not null default 'new' check (status in ('new', 'confirmed', 'packed', 'shipped', 'done', 'cancelled')),
  customer_name text not null,
  customer_phone text not null,
  customer_email text,
  customer_city text,
  total integer not null,
  items jsonb not null,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

create table if not exists public.site_content (
  key text primary key,
  value jsonb not null,
  updated_at timestamptz default now()
);

