-- Public read for storefront
drop policy if exists "products_select_public" on public.products;
create policy "products_select_public" on public.products
for select
using (true);

drop policy if exists "site_content_select_public" on public.site_content;
create policy "site_content_select_public" on public.site_content
for select
using (true);

-- Public create for checkout
drop policy if exists "orders_insert_public" on public.orders;
create policy "orders_insert_public" on public.orders
for insert
with check (true);

-- Admin access (authenticated users)
drop policy if exists "products_manage_auth" on public.products;
create policy "products_manage_auth" on public.products
for all
using (auth.role() = 'authenticated')
with check (auth.role() = 'authenticated');

drop policy if exists "site_content_manage_auth" on public.site_content;
create policy "site_content_manage_auth" on public.site_content
for all
using (auth.role() = 'authenticated')
with check (auth.role() = 'authenticated');

drop policy if exists "orders_manage_auth" on public.orders;
create policy "orders_manage_auth" on public.orders
for select
using (auth.role() = 'authenticated');

drop policy if exists "orders_update_auth" on public.orders;
create policy "orders_update_auth" on public.orders
for update
using (auth.role() = 'authenticated')
with check (auth.role() = 'authenticated');

drop policy if exists "orders_delete_auth" on public.orders;
